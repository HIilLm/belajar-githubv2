    private void btnhitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhitungActionPerformed
        try {
        int sepatu = 0,kostum =0,tambahan,lama,lapangan;
        String output;
        lama = Integer.parseInt(txtlamasewa.getText());
        if(cksepatu.isSelected()){
            sepatu = 50000;
        }
        
        if(ckkostum.isSelected()){
            kostum = 45000;
        }
        
        tambahan = (sepatu + kostum) * lama;
        
        lapangan = Integer.parseInt(lblbayar.getText());
        total = (lapangan * lama) + (tambahan *lama);
        output = Integer.toString(total);
        txttotal.setText(output);
        } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Isi Terlebih Dahulu");
        }
    }//GEN-LAST:event_btnhitungActionPerformed

