    private void txtbayarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbayarKeyReleased
        try {
            String output;
            tunai = Integer.parseInt(txtbayar.getText());
          if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
                kembalian = tunai - total;
                output = Integer.toString(kembalian);
                if(!"".equals(txttotal.getText())){
                    if(kembalian < 0){
                        txtkembalian.setText("Uang Tidak Cukup"); 
                    } else {
                        txtkembalian.setText(output);                                            
                    }
                } 
                else {
                    JOptionPane.showMessageDialog(this, "Hitung Terlebih Dahulu");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Isi Terlebih Dahulu");
        }
    }//GEN-LAST:event_txtbayarKeyReleased

